"""
================================================================================
student/synthetic_dataset.py
================================================================================

Dataset and DataLoader utilities for the Student Model.
Loads the synthetic data generated by the Anchor model from Apache Arrow format.
"""

from pathlib import Path
from typing import Tuple, Optional
import torch
from torch.utils.data import Dataset, DataLoader
from datasets import load_from_disk
from student_model import get_tokenizer

class SyntheticLMDataset(Dataset):
    """
    PyTorch Dataset wrapper for HuggingFace's Arrow dataset.
    Extracts generated_text, tokenizes it on the fly, and creates attention_mask and labels.
    """
    def __init__(self, hf_dataset, tokenizer, max_length: int = 1024, max_samples: Optional[int] = None):
        if max_samples is not None:
            self.dataset = hf_dataset.select(range(min(max_samples, len(hf_dataset))))
        else:
            self.dataset = hf_dataset
        self.tokenizer = tokenizer
        self.max_length = max_length

    def __len__(self) -> int:
        return len(self.dataset)

    def __getitem__(self, idx: int) -> dict:
        item = self.dataset[idx]
        text = item["generated_text"]
        
        # Tokenize the text
        encodings = self.tokenizer(
            text,
            truncation=True,
            max_length=self.max_length,
            padding="max_length",
            return_tensors="pt"
        )
        
        input_ids = encodings["input_ids"].squeeze(0)
        attention_mask = encodings["attention_mask"].squeeze(0)
        
        return {
            "input_ids": input_ids,
            "attention_mask": attention_mask,
            "labels": input_ids.clone()
        }

def build_dataloaders(
    data_dir: Path,
    batch_size: int,
    val_ratio: float,
    max_samples: Optional[int] = None,
    num_workers: int = 0
) -> Tuple[DataLoader, DataLoader]:
    """
    Load the synthetic dataset from disk, tokenize it, and split into loaders.
    """
    hf_dataset = load_from_disk(str(data_dir))
    tokenizer = get_tokenizer()
    
    # Shuffle dataset
    hf_dataset = hf_dataset.shuffle(seed=42)
    
    if max_samples:
        hf_dataset = hf_dataset.select(range(min(max_samples, len(hf_dataset))))
        
    n_total = len(hf_dataset)
    n_val = max(1, int(n_total * val_ratio))
    n_train = n_total - n_val
    
    # Split dataset
    train_dataset = SyntheticLMDataset(hf_dataset.select(range(n_train)), tokenizer)
    val_dataset = SyntheticLMDataset(hf_dataset.select(range(n_train, n_total)), tokenizer)
    
    train_loader = DataLoader(
        train_dataset,
        batch_size=batch_size,
        shuffle=True,
        num_workers=num_workers,
        pin_memory=torch.cuda.is_available(),
        drop_last=True
    )
    
    val_loader = DataLoader(
        val_dataset,
        batch_size=batch_size,
        shuffle=False,
        num_workers=num_workers,
        pin_memory=torch.cuda.is_available(),
        drop_last=False
    )
    
    return train_loader, val_loader
